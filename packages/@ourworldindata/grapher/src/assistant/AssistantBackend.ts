const MAX_LISTED_ENTITIES = 3

/**
 * Deterministic template that turns a view into a short description,
 * e.g. "Map · 2024" or "Line chart · Nigeria, Ethiopia · 1990–2020".
 * This is the only way the assistant produces text about a view.
 */
export function describeView(
    view: AssistantView,
    context: AssistantChartContext
): string {
    const parts: string[] = []

    const tab = view.tab ?? context.activeTab
    parts.push(TAB_LABELS[tab] ?? tab)

    if (view.mapRegion && view.mapRegion !== MapRegionName.World)
        parts.push(MAP_REGION_LABELS[view.mapRegion])

    if (view.entitySummary) {
        parts.push(view.entitySummary)
    } else if (view.entityNames && view.entityNames.length > 0) {
        const listed = view.entityNames.slice(0, MAX_LISTED_ENTITIES)
        const more = view.entityNames.length - listed.length
        parts.push(
            more > 0 ? `${listed.join(", ")} + ${more} more` : listed.join(", ")
        )
    }

    const time = describeViewTime(view)
    if (time) parts.push(time)

    return parts.join(" · ")
}

function describeViewTime(view: AssistantView): string | undefined {
    const { startTime, endTime } = view
    if (startTime !== undefined && endTime !== undefined)
        return startTime === endTime ? `${endTime}` : `${startTime}–${endTime}`
    if (startTime !== undefined) return `Since ${startTime}`
    if (endTime !== undefined) return `Until ${endTime}`
    return undefined
}

/** Turns a view into Grapher query params that can be applied to the chart */
export function viewToQueryParams(
    view: AssistantView,
    context: AssistantChartContext
): GrapherQueryParams {
    const params: GrapherQueryParams = {}

    const effectiveTab = view.tab ?? context.activeTab
    if (view.tab) params.tab = TAB_QUERY_PARAM_VALUES[view.tab]

    if (view.entityNames && view.entityNames.length > 0) {
        // Selecting the World entity on the map is a no-op, skip it
        const isWorldOnMap =
            effectiveTab === GRAPHER_TAB_NAMES.WorldMap &&
            view.entityNames.every(isWorldEntityName)
        if (!isWorldOnMap)
            params.country = generateSelectedEntityNamesParam(view.entityNames)
    }

    if (effectiveTab === GRAPHER_TAB_NAMES.WorldMap) {
        // Make map views deterministic: always set the map zoom, and either
        // highlight the requested entities or clear a stale map selection
        params.region = view.mapRegion ?? MapRegionName.World
        params.mapSelect = params.country ?? ""
    }

    const { startTime, endTime } = view
    if (startTime !== undefined && endTime !== undefined)
        params.time =
            startTime === endTime ? `${endTime}` : `${startTime}..${endTime}`
    else if (startTime !== undefined) params.time = `${startTime}..latest`
    else if (endTime !== undefined) params.time = `earliest..${endTime}`

    return params
}

export function makeViewOption(
    view: AssistantView,
    headline: string,
    context: AssistantChartContext
): AssistantViewOption {
    return {
        view,
        params: viewToQueryParams(view, context),
        headline,
        description: describeView(view, context),
    }
}

function makeApplyResponse(
    view: AssistantView,
    context: AssistantChartContext
): AssistantResponse {
    return {
        kind: "apply",
        view,
        params: viewToQueryParams(view, context),
        description: describeView(view, context),
    }
}

