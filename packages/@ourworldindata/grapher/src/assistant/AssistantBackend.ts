/**
 * Prototype backend for the "AI assistant (BETA)" panel on data pages.
 *
 * The assistant never returns freeform prose. Every response is either:
 * - "apply": a single concrete chart view (as Grapher query params) that is
 *   applied immediately, plus a templated confirmation string, or
 * - "options": a list of concrete chart views the user can click.
 *
 * All descriptions are generated by the deterministic `describeView` template
 * below — never authored freely by the backend. A future LLM-based backend
 * should implement `AssistantBackend` and produce `AssistantView` objects that
 * are run through the same template, so it also can't emit prose.
 */

/** Chart context handed to the backend alongside the user's query */
export interface AssistantChartContext {
    availableEntityNames: EntityName[]
    availableTabs: GrapherTabName[]
    activeTab: GrapherTabName
    /** All times with data, sorted ascending */
    availableTimes: Time[]
    selectedEntityNames: EntityName[]
    /**
     * Latest data value per country entity (aggregates excluded), used to
     * answer "top/bottom N" queries
     */
    latestValueByEntityName: Map<EntityName, number>
    /**
     * Per-country change (last value minus first value) over a trailing
     * window of the data, used for "fastest decline/increase" options
     */
    trailingChange?: AssistantTrailingChange
}

export interface AssistantTrailingChange {
    startTime: Time
    endTime: Time
    /** Last value minus first value within the window, per country entity */
    changeByEntityName: Map<EntityName, number>
}

/** A structured description of a chart view, the assistant's only "language" */
export interface AssistantView {
    tab?: GrapherTabName
    entityNames?: EntityName[]
    /**
     * Optional templated summary shown instead of listing all entity names,
     * e.g. "All countries in Africa (54)". Must be produced by a deterministic
     * template, never authored freely.
     */
    entitySummary?: string
    startTime?: Time
    endTime?: Time
    /** Map region to zoom to (map tab only) */
    mapRegion?: MapRegionName
}

export interface AssistantViewOption {
    /** The structured view this option maps to */
    view: AssistantView
    params: GrapherQueryParams
    /**
     * Short motivation shown as the chip's first line, produced by a
     * deterministic template tied to a computable selection (never prose)
     */
    headline: string
    /** The grapher mapping (describeView output), the chip's second line */
    description: string
}

export type AssistantResponse =
    | {
          kind: "apply"
          view: AssistantView
          params: GrapherQueryParams
          description: string
      }
    | { kind: "options"; options: AssistantViewOption[]; note?: string }

export interface AssistantBackend {
    respond(
        query: string,
        context: AssistantChartContext
    ): Promise<AssistantResponse>
}

